let activeIndex = 0;

function renderSidebar() {
  const nav = document.getElementById('sidebarNav');
  nav.innerHTML = '';
  animals.forEach((animal, i) => {
    const btn = document.createElement('button');
    btn.className = 'zoos-sidebar__item' + (i === activeIndex ? ' active' : '');
    btn.setAttribute('aria-label', animal.title);
    btn.innerHTML = `<span class="zoos-sidebar__icon"><img src="${animal.icon}" alt="${animal.id}"></span><span class="zoos-sidebar__label">${animal.sidebarLabel}</span>`;
    btn.addEventListener('click', () => {
      activeIndex = i;
      renderSidebar();
      renderContent();
    });
    nav.appendChild(btn);
  });
}

const sidebar = document.querySelector('.zoos-sidebar');
const expandBtn = document.querySelector('.zoos-sidebar__expand');
const sidebarOverlay = document.getElementById('zoosSidebarOverlay');
const sidebarTrigger = document.getElementById('zoosSidebarTrigger');

function openZoosSidebar() {
  sidebar.classList.add('is-expanded');
}

function closeZoosSidebar() {
  sidebar.classList.remove('is-expanded');
}

expandBtn.addEventListener('click', () => {
  if (sidebar.classList.contains('is-expanded')) {
    closeZoosSidebar();
  } else {
    openZoosSidebar();
  }
});

if (sidebarOverlay) {
  sidebarOverlay.addEventListener('click', closeZoosSidebar);
}

if (sidebarTrigger) {
  sidebarTrigger.addEventListener('click', openZoosSidebar);
}

function renderContent() {
  const animal = animals[activeIndex];

  document.getElementById('animalTitle').textContent = animal.title;
  document.getElementById('playerLabel').textContent = animal.playerLabel;

  const playerImg = document.getElementById('playerImg');
  playerImg.src = animal.playerImg;
  playerImg.alt = animal.title;

  const camCards = document.getElementById('camCards');
  camCards.innerHTML = '';
  animal.cams.forEach((cam, i) => {
    const card = document.createElement('div');
    card.className = 'zoos-cam-card' + (i === 0 ? ' active' : '');
    card.innerHTML = `
      <img src="${cam.img}" alt="${cam.label}">
      <div class="zoos-cam-card__label">${cam.label} <span class="zoos-cam-card__lock"></span></div>
      <div class="zoos-cam-card__play"></div>
    `;
    camCards.appendChild(card);
  });

  document.getElementById('donationTitle').textContent = animal.donationTitle;
  document.getElementById('donationText').textContent = animal.donationText;
  document.getElementById('factText').textContent = animal.fact;

  const infoTable = document.getElementById('infoTable');
  infoTable.innerHTML = '';
  Object.entries(animal.info).forEach(([key, value]) => {
    const tr = document.createElement('tr');
    if (key === 'Range:') {
      tr.innerHTML = `<td>${key}</td><td>${value} <a class="zoos-info__map-link" href="../map/">View Map</a></td>`;
    } else {
      tr.innerHTML = `<td>${key}</td><td>${value}</td>`;
    }
    infoTable.appendChild(tr);
  });

  const infoPhoto = document.getElementById('infoPhoto');
  infoPhoto.src = animal.photo;
  infoPhoto.alt = animal.title;

  document.getElementById('description').textContent = animal.description;
}

renderSidebar();
renderContent();

(function () {
  const headerBurger = document.getElementById("headerBurger");
  const sideNav = document.getElementById("sideNav");
  const sideNavOverlay = document.getElementById("sideNavOverlay");
  const sideNavClose = document.getElementById("sideNavClose");

  function openSideNav() {
    sideNav.classList.add("is-open");
    document.body.style.overflow = "hidden";
  }

  function closeSideNav() {
    sideNav.classList.remove("is-open");
    document.body.style.overflow = "";
  }

  headerBurger.addEventListener("click", openSideNav);
  sideNavClose.addEventListener("click", closeSideNav);
  sideNavOverlay.addEventListener("click", closeSideNav);

  document.addEventListener("keydown", function (e) {
    if (e.key === "Escape") { closeSideNav(); }
  });
})();



